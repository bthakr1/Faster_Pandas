def mathOp(a,b):
    classic_division = a / b
    floor_division = a // b
    modulus = a % b
    power = a ** b
    return [classic_division,floor_division,modulus,power]


# check if the number is odd or even

def evenOdd(n):
    if n % 2 == 0:
        print("Even")
    else:
        print("odd")

# Check parity

def checkParity(n):
    result = (n % 2)
    return result

def inRange(x,y):
    return (x < 1/3 < y)

def getStr(s):
    s = s[:1] + s[0] + s[1:]
    s = s[:1] + s[0] + s[1:]
    s = s[:3] + s[3] + s[3:]
    s = s[:3] + s[3] + s[3:]
    s = s[:6] + s[6] + s[6:]
    s = s[:6] + s[6] + s[6:]

    strlen = len(s)
    return [s,strlen]


def findOccurence(s):
    a = s.find("b")
    b = s.find("ccc")
    return [a,b]

def changeCase(s):
    upper_case = s.upper()
    lower_case = s.lower()
    return [upper_case,lower_case]
